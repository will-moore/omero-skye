<h1>OMERO-skye</h1>

<p>
	This is a <a href="https://kit.svelte.dev/">SvelteKit</a> based project for browsing data from OMERO.
	Named "skye" because it starts with SK and Skye is a favourite place in Scotland!
</p>

<p>
	Initially designed to load data from <a href="https://idr.openmicroscopy.org/">IDR</a>.
</p>

<p>
	See the <a target="_blank" href="https://github.com/will-moore/omero-skye"
		>omero-skye project on GitHub</a
	>.
</p>
